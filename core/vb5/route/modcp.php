class vB5_Route_Modcp extends vB5_Route
{
	public function getUrl()
	{
		$url = '/' . $this->prefix . '/' . $this->arguments['file'] . '.php';

		if (strtolower(vB_String::getCharset()) != 'utf-8')
		{
			$url = vB_String::encodeUtf8Url($url);
		}

		return $url;
	}
}

